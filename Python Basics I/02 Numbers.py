# Fundamental Data Types
# int and float

print(2 + 4)
print(2 - 4)    
print(2 * 4)
print(2 / 4) 

print(type(2 + 4))
print(type(2 / 4))

print(type(20 + 1.1))
print(type(1.5 + 1.5))

print(2 ** 3) # 2 to the power of 3
print(5 // 4) # Floor division (rounds down to nearest whole number)
print(6 % 4) # Modulus (remainder after division)