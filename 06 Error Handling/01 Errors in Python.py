# Exceptions are error conditions that occur during the execution of a program.
# They can be caused by various factors, such as invalid input, file not found, or network issues.

# Different types of error:
# 1. SyntaxError: Occurs when the code is not written correctly according to Python's syntax rules. 
# 2. NameError: Occurs when a variable or function name is not defined.
# 3. TypeError: Occurs when an operation or function is applied to an object of inappropriate type.
# 4. IndexError: Occurs when trying to access an index that is out of range in a list or string.
# 5. KeyError: Occurs when trying to access a dictionary with a key that does not exist.
# 6. ValueError: Occurs when a function receives an argument of the right type but inappropriate value.
# 7. AttributeError: Occurs when an invalid attribute reference is made.
# 8. ZeroDivisionError: Occurs when dividing by zero.
# 9. ImportError: Occurs when an import statement fails to find the module definition or name.
# 10. IOError: Occurs when an I/O operation fails, such as file not found or permission denied.