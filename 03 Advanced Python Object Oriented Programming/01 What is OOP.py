# Object-Oriented Programming (OOP) in Python

# OOP is a programming paradigm that uses objects and classes to structure code.
# It helps in modularity, code reuse, and abstraction.

# Class
# A class is a blueprint or template for creating objects.
# It defines attributes (variables) and methods (functions) that describe an entity.

# Object
# An object is an instance of a class.
# It represents a specific entity with attributes and behaviors defined by the class.

# Paradigm
# A paradigm is a programming style or approach.
# OOP is one of the paradigms in Python, along with Procedural and Functional Programming.

# Instantiation
# Instantiation is the process of creating an object from a class.
# When an object is instantiated, it is assigned memory and can use the attributes and methods of the class.

